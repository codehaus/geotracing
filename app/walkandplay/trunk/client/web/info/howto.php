<h2>Howto</h2>
<hr />
<h2>Create</h2>
<p>You can create a mobile game yourself by placing different types of markers onto the map. First you have to create a game by giving it a name, description, intro and outro. Then you set your markers. Markers can either be a task, text or medium. A task is always answered by sending in the right answer - you can multiple answers if they're separated by a comma - and media (a photo, audio or text).
All markers you place are seen on the mobile screen once you play.</p>
<p><img src="/dev/info/images/place-location.jpg" border="0" /></p>
<p>After you have placed all your markers you need to add a gameround which you have to assign to (multiple) players.</p>

<h2>Play</h2>
<p>You can play together with a web player, but the mobile player is always leading: markers only become visible once they have hit by a mobile player. Once a task is hit both web and mobile player can send in the answer, but only the mobile player can send in media. Web and mobile player can communicate using instant messaging.</p>

