<h2>Challenges</h2>
<h3>Goals</h3>
<p>This is the most general sort of victory condition, which can be broad enough to encompass any method of winning, but here refers to game-specific goals that are usually not duplicated in other games. An example is the checkmate of a king in chess.</p>
<h3>Piece elimination</h3>
<p>Some games with capture mechanics are won by the player who removes all, or a given number of, the opponents' playing pieces.</p>
<h3>Puzzle guessing</h3>
<p>Some games end when a player guesses (or solves by logic) the answer to a puzzle or riddle posed by the game. The player who guesses successfully wins. Examples include hangman and zendo.</p>
<h3>Races</h3>
<p>Many simple games (and some complex ones) are effectively races. The first player to advance one or more tokens to or beyond a certain point on the board wins. Examples: backgammon, ludo.</p>
<h3>Structure building</h3>
<p>The goal of a structure building game is to acquire and assemble a set of game resources into either a defined winning structure, or into a structure that is somehow better than those of other players. In some games, the acquisition is of primary importance (e.g. concentration), while in others the resources are readily available and the interactions between them form more or less useful structures (e.g. poker).</p>
<h3>Territory control</h3>
<p>A winner may be decided by which player controls the most &quot;territory&quot; on the playing surface, or a specific piece of territory. This is common in wargames, but is also used in more abstract games such as go.</p>
<h3>Victory points</h3>
<p>The winner can be decided either by:</p>
<p> * The first player to reach a set number of points.<br />
  * The player with the most points at a predetermined finishing time or state of the game.</p>
